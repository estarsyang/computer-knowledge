# include <stdio.h>

// 整数输出,例如 12345 输出 1 2 3 4 5
int main() {
    // x =12345;
    // 12345/10000 --> 1
    // 12345%10000 --> 2345
    // 10000/10    --> 1000
    // 2345/1000   --> 2
    // 2345%1000   --> 345
    // 1000/10     --> 100
    // 345/100     --> 3
    // 345%100     --> 45
    // 100/10      --> 10
    // 45/10       --> 4
    // 45%10       --> 5
    // 10/10       --> 1
    // 5/1         --> 5
    // 5%1         --> 0
    // 1/10        --> 0
    int x = 0;
    printf("请输入一个正整数\n");
    scanf("%d", &x);
    int mask = 1;
    int t = x;
    while(t > 9) {
        t /= 10;
        mask *=10;
    }

    while (mask > 0) {
      int d = x / mask;
      printf("%d",d);
      if(mask > 9) {
        printf(" ");
      }
      x %=mask;
      mask /=10;
    }

    return 0;
}
